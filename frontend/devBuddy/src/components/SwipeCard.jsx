const SwipeCards = () => {
  const [cards, setCards] = useState(cardData);

  return (
    <div className="grid h-[500px] w-full place-items-center bg-gray-100">
      {cards.map((card) => {
        return (
          <Card key={card.id} cards={cards} setCards={setCards} {...card} />
        );
      })}
    </div>
  );
};

const Card = ({ id, url, setCards, cards }) => {
  const x = useMotionValue(0);

  const rotateRaw = useTransform(x, [-150, 150], [-18, 18]);
  const opacity = useTransform(x, [-150, 0, 150], [0, 1, 0]);

  const isFront = id === cards[cards.length - 1].id;

  const rotate = useTransform(() => {
    const offset = isFront ? 0 : id % 2 ? 6 : -6;
    return `${rotateRaw.get() + offset}deg`;
  });

  const handleDragEnd = () => {
    const threshold = 100;
    if (Math.abs(x.get()) > threshold) {
      setCards((pv) => pv.filter((v) => v.id !== id));
    }
  };

  return (
    <motion.img
      src={url}
      alt="Placeholder alt"
      className="h-96 w-72 origin-bottom rounded-lg bg-white object-cover select-none"
      style={{
        gridRow: 1,
        gridColumn: 1,
        x,
        opacity: isFront ? 1 : 0.8,
        rotate,
        boxShadow: isFront
          ? "0 20px 25px -5px rgb(0 0 0 / 0.5), 0 8px 10px -6px rgb(0 0 0 / 0.5)"
          : "0 4px 6px -1px rgb(0 0 0 / 0.1)",
        cursor: isFront ? "grab" : "default",
        zIndex: isFront ? 10 : cards.length - id,
      }}
      animate={{
        scale: isFront ? 1 : 0.95 - (cards.length - id) * 0.02,
      }}
      drag={isFront ? "x" : false}
      dragConstraints={{
        left: -200,
        right: 200,
      }}
      dragElastic={0.2}
      whileDrag={{
        cursor: "grabbing",
        scale: 1.05,
      }}
      onDragEnd={handleDragEnd}
      transition={{
        type: "spring",
        stiffness: 300,
        damping: 30,
      }}
    />
  );
};

export default SwipeCards;